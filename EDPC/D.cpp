int N, W;

vector<vector<ll>> memo;

ll solve(vector<pair<ll, ll>>& P, int pos,int w) {
    if (w < 0)return -INF;
    if (pos == 0)return 0;
    if (memo[pos][w] != -INF)return memo[pos][w];

    auto [ww, v] = P[pos];

    return memo[pos][w] = max(solve(P, pos - 1, w), solve(P, pos - 1, w - ww) + v);
}


